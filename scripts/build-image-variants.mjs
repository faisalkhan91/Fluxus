#!/usr/bin/env node
/**
 * Generates width-variant WebP files for the responsively-sized raster images
 * (portfolio screenshots + blog covers/inline figures) and writes a manifest
 * the runtime IMAGE_LOADER consumes (src/app/core/image/image-variants.generated.ts).
 *
 * Without a loader, NgOptimizedImage emits no usable srcset, so a 1519 px
 * screenshot is downloaded into a 12vw card. With this manifest + loader,
 * Angular auto-emits a width-descriptor srcset and the browser picks the
 * smallest variant that covers the slot.
 *
 * Variants are committed (so `ng serve` and CI both work without a prebuild
 * step) and this script is idempotent — it skips any variant that already
 * exists and is newer than its source, so re-running in build:prod is a no-op.
 *
 * Run via `npm run build:image-variants` (chained from build:prod).
 */
import { statSync, existsSync } from 'node:fs';
import { join, relative, sep, posix } from 'node:path';
import { fileURLToPath } from 'node:url';
import sharp from 'sharp';
import { walk } from './lib/fs.mjs';
import { writeFormatted } from './lib/emit.mjs';

const SRC_ROOT = fileURLToPath(new URL('../src', import.meta.url));
const DIRS = ['assets/images/portfolio', 'assets/images/blog'].map((d) => join(SRC_ROOT, d));
const OUT = fileURLToPath(
  new URL('../src/app/core/image/image-variants.generated.ts', import.meta.url),
);

// Width breakpoints to emit. Tuned to the slots these images render in:
// 12vw "more work" rows, 33vw grid cards, 50vw list heroes, 60vw detail
// heroes, 25vw blog covers, and full-bleed prose figures — across 1x/2x DPR
// and the mobile (~92vw) layout. Only widths strictly smaller than a source's
// intrinsic width are generated (no upscaling).
const WIDTHS = [256, 384, 640, 960, 1280];
const QUALITY = 82;

const isVariant = (f) => /-\d+w\.webp$/i.test(f);
const variantPath = (abs, w) => abs.replace(/\.webp$/i, `-${w}w.webp`);
const toKey = (abs) => relative(SRC_ROOT, abs).split(sep).join(posix.sep);

const manifest = {};
let made = 0;
let skipped = 0;

for (const dir of DIRS) {
  if (!existsSync(dir)) continue;
  for (const file of walk(dir)) {
    if (!file.toLowerCase().endsWith('.webp') || isVariant(file)) continue;
    const meta = await sharp(file).metadata();
    // Skip animated WebP (e.g. StormEvents) — resizing per-frame is costly and
    // these render small anyway; the loader falls back to the original.
    if ((meta.pages ?? 1) > 1) continue;
    const intrinsic = meta.width ?? 0;
    const widths = WIDTHS.filter((w) => w < intrinsic);
    if (widths.length === 0) continue;

    const srcMtime = statSync(file).mtimeMs;
    for (const w of widths) {
      const dest = variantPath(file, w);
      if (existsSync(dest) && statSync(dest).mtimeMs >= srcMtime) {
        skipped++;
        continue;
      }
      await sharp(file)
        .resize({ width: w, withoutEnlargement: true })
        .webp({ quality: QUALITY, effort: 6 })
        .toFile(dest);
      made++;
    }
    manifest[toKey(file)] = widths;
  }
}

const ordered = Object.fromEntries(Object.entries(manifest).sort(([a], [b]) => a.localeCompare(b)));

const banner = `// AUTO-GENERATED by scripts/build-image-variants.mjs — do not hand-edit.
// Re-run via \`npm run build:image-variants\`.

`;
const rawBody =
  banner +
  '/**\n * Maps a source image path to the variant widths generated for it.\n' +
  ' * Consumed by the responsive IMAGE_LOADER to rewrite NgOptimizedImage\n' +
  ' * srcset entries to the nearest available variant. Images absent here are\n' +
  ' * served as-is (the loader returns the original src untouched).\n */\n' +
  `export const IMAGE_VARIANTS: Record<string, readonly number[]> = ${JSON.stringify(ordered, null, 2)};\n`;

await writeFormatted(OUT, rawBody);

console.log(
  `Wrote ${OUT}\n  ${Object.keys(ordered).length} source images, ` +
    `${made} variant(s) generated, ${skipped} up-to-date.`,
);
