#!/usr/bin/env node
/**
 * Walks every image under src/assets/images/, queries each one's intrinsic
 * dimensions via sharp, and writes a generated TypeScript map at
 * src/app/core/services/image-dims.generated.ts.
 *
 * MarkdownService consumes this map at runtime (and SSR) so every blog-rendered
 * <img> can carry width/height — which is what prevents CLS.
 *
 * Run via `npm run build:image-dims` (also chained from `build:prod`).
 */
import { readdirSync, statSync, writeFileSync } from 'node:fs';
import { join, relative, sep, posix } from 'node:path';
import { fileURLToPath } from 'node:url';
import prettier from 'prettier';
import sharp from 'sharp';

const ROOT = fileURLToPath(new URL('../src/assets/images', import.meta.url));
const OUT = fileURLToPath(
  new URL('../src/app/core/services/image-dims.generated.ts', import.meta.url),
);

const SUPPORTED = new Set(['.webp', '.png', '.jpg', '.jpeg', '.gif', '.avif', '.svg']);

function* walk(dir) {
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) {
      yield* walk(full);
    } else {
      yield full;
    }
  }
}

function toPosixAssetPath(absolute) {
  // We want keys like `assets/images/blog/foo.webp`, identical to what the
  // markdown source authors as `![](assets/images/blog/foo.webp)`.
  const rel = relative(fileURLToPath(new URL('../src', import.meta.url)), absolute);
  return rel.split(sep).join(posix.sep);
}

const dims = {};
let processed = 0;

for (const file of walk(ROOT)) {
  const ext = file.slice(file.lastIndexOf('.')).toLowerCase();
  if (!SUPPORTED.has(ext)) continue;
  try {
    const meta = await sharp(file).metadata();
    if (meta.width && meta.height) {
      dims[toPosixAssetPath(file)] = { w: meta.width, h: meta.height };
      processed++;
    }
  } catch (err) {
    console.warn(`  skipped ${toPosixAssetPath(file)} — ${err.message}`);
  }
}

// Sort keys for stable diffs.
const ordered = Object.fromEntries(Object.entries(dims).sort(([a], [b]) => a.localeCompare(b)));

const banner = `// AUTO-GENERATED by scripts/build-image-dims.mjs — do not hand-edit.
// Re-run via \`npm run build:image-dims\`.

`;

const rawBody =
  banner +
  'export interface ImageDim {\n  /** intrinsic width in CSS px */\n  w: number;\n  /** intrinsic height in CSS px */\n  h: number;\n}\n\n' +
  `export const IMAGE_DIMS: Record<string, ImageDim> = ${JSON.stringify(ordered, null, 2)};\n`;

// Format through prettier using the project config so the diff stays minimal
// across regenerations and `npm run lint` (which calls `prettier --check`)
// stays green. Falling back to the raw JSON.stringify output keeps the script
// usable if prettier isn't installed; CI will surface the drift in that case.
let body = rawBody;
try {
  const config = (await prettier.resolveConfig(OUT)) ?? {};
  body = await prettier.format(rawBody, { ...config, filepath: OUT });
} catch {
  /* prettier unavailable — emit raw output and let lint flag the drift */
}

writeFileSync(OUT, body, 'utf-8');
console.log(`Wrote ${OUT} with ${processed} image dim entries.`);
