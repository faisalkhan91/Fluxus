#!/usr/bin/env node
/**
 * Writes a build-time version stamp consumed by AppUpdateService.
 *
 * Two outputs, both committed to the repo (matching the existing
 * image-dims.generated.ts convention):
 *
 *   1. src/version.json
 *      Static asset shipped under `dist/fluxus/browser/version.json`.
 *      AppUpdateService fetches it with `cache: 'no-store'` on tab focus
 *      and on a 5-minute interval; if the returned `id` no longer matches
 *      the bundled APP_VERSION, a deploy has shipped and the service
 *      reloads at the next safe boundary.
 *
 *   2. src/app/core/services/app-version.generated.ts
 *      Exports the same `id` so the JS bundle ships with its own build
 *      stamp baked in. Comparing fetched-vs-baked is enough to detect
 *      "stale shell, fresh server" without depending on Angular's
 *      SwUpdate (which can lag, be disabled, or never wake up on short
 *      visits).
 *
 * Run via `npm run build:version-stamp` (also chained from `build:prod`).
 *
 * The `id` is `<short-sha>-<epoch-ms>` so two builds at the same SHA
 * (e.g. a force-rebuild without code changes) still produce a different
 * stamp; this is what the user perceives as "I just deployed", regardless
 * of whether the diff was empty.
 */
import { writeFileSync } from 'node:fs';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const VERSION_JSON = fileURLToPath(new URL('../src/version.json', import.meta.url));
const VERSION_TS = fileURLToPath(
  new URL('../src/app/core/services/app-version.generated.ts', import.meta.url),
);

function shortSha() {
  try {
    return execSync('git rev-parse --short HEAD', { stdio: ['ignore', 'pipe', 'ignore'] })
      .toString()
      .trim();
  } catch {
    // Builds can run outside a git checkout (e.g. tarball releases). Fall
    // back to a stable placeholder; the epoch component still varies.
    return 'nogit';
  }
}

const sha = shortSha();
const builtAt = new Date().toISOString();
const id = `${sha}-${Date.now()}`;

writeFileSync(VERSION_JSON, JSON.stringify({ id, sha, builtAt }, null, 2) + '\n', 'utf-8');

// Emit single-quoted string literals so the file passes the project's
// Prettier check without a follow-up `--write` pass. The build id and ISO
// timestamp are guaranteed not to contain `'`, so straight substitution
// is safe here (no escaping pass needed).
const tsBody = `// AUTO-GENERATED by scripts/build-version-stamp.mjs - do not hand-edit.
// Re-run via \`npm run build:version-stamp\`.

/** Build identifier baked into the JS bundle at compile time. */
export const APP_VERSION = '${id}';

/** ISO-8601 timestamp of the build, surfaced for diagnostics only. */
export const APP_BUILT_AT = '${builtAt}';
`;
writeFileSync(VERSION_TS, tsBody, 'utf-8');

console.log(`Wrote version stamp ${id} (${builtAt}).`);
